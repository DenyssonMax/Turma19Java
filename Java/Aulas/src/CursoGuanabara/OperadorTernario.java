package CursoGuanabara;

public class OperadorTernario {
	
	public static void main(String args []) {
		
		int n1, n2, r;
		n1 = 12;
		n2 = 3;
		r = (n1>n2) ? 0:1;   // antes da ? tem a condição. e depois o que vai acontecer caso a condição seja verdaira ou falsa
		
		
		System.out.println("O valor para sua expressão é: "+ r);
		
	}

}
